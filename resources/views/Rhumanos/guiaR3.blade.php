    <h6>Identificación y análisis de los factores de riesgo psicosocial</h6>




    <p>El contenido de esta guía es un complemento para la mejor comprensión de la presente Norma, puede ser utilizada
        por aquellos centros de trabajo que cuenten con más de 50 trabajadores.</p>
    <p>La identificación y análisis de los factores de riesgo psicosocial a que se refiere el numeral 8.1, inciso a), de
        la presente Norma, se podrá realizar aplicando el cuestionario de esta guía.</p>
    <p><span
            class="text-primary">III.1 El cuestionario deberá aplicarse a todos los trabajadores del centro de trabajo, o bien, se podrá aplicar a una muestra representativa de éstos. El tamaño de la muestra se deberá determinar conforme a la Ecuación 1 siguiente:</span>
    </p>
    <div class="text-center">
        <img alt="" style="border: solid 0px;  " width="448" height="140" class="rounded"
             src="../resources/images/gr3c1.png">
    </div>

    <p>En donde:</p>
    <p>N es el número total de trabajadores del centro de trabajo.</p>
    <p>n es el número de trabajadores a los que se les deberán aplicarán los cuestionarios.</p>
    <p>0.9604 y 0.0025 permanecen constantes.</p>
    <p>La selección de los trabajadores deberá realizarse de forma aleatoria, de manera que todos los trabajadores
        puedan ser considerados para la aplicación de los cuestionarios, sin importar, su turno, tipo de puesto, área de
        trabajo, departamento, etc.</p>
    <p>La muestra se distribuirá entre hombres y mujeres conforme al porcentaje de cada género en el centro de
        trabajo.</p>
    <p><span class="text-primary">III.2 Para la aplicación del cuestionario deberán cumplirse, al menos, las condiciones siguientes:</span>
    </p>
    <p><span class="text-info">a)    Antes de iniciar la aplicación del cuestionario deberá:</span>
    <p>1) Determinar el número mínimo de trabajadores a los que se les aplicarán los cuestionarios que al menos deberá
        corresponder con el tamaño de la muestra calculado con la Ecuación 1 de la presente Norma;</p>
    <p>2) Verificar que las condiciones de las instalaciones y mobiliario sean idóneas;</p>
    <p>3) Realizar la presentación del aplicador ante las personas a evaluar;</p>
    <p>4) Explicar el objetivo de la evaluación;</p>
    <p>5) Enfatizar en la protección de la privacidad y confidencialidad del manejo de los datos, y que el uso de la
        información proporcionada por el trabajador (ver Guía de referencia V Datos del trabajador) y de sus resultados
        será exclusivamente para fines de mejora del ambiente de trabajo;</p>
    <p>6) Dar instrucciones claras sobre: la forma de responder a las preguntas de las diferentes secciones; que se
        conteste el cuestionario completamente; que no existen respuestas correctas o incorrectas; que es necesaria su
        concentración; que se consideren las condiciones de los dos últimos meses, y que su opinión es lo más importante
        por lo que se le pide que conteste con sinceridad, y</p>
    <p>7) Iniciar la aplicación en el orden siguiente: en primer lugar, del cuestionario sobre factores de riesgo
        psicosocial, y finalmente, realizar el llenado de la ficha de datos generales sobre el trabajador.</p>
    <p><span class="text-info">b)    Durante la aplicación del cuestionario se deberá:</span></p>
    <p>1) Propiciar un ambiente de respeto y confianza;</p>
    <p>2) Permitir una comunicación fluida entre los trabajadores y evaluador;</p>
    <p>3) Aclarar dudas y brindar apoyo a los trabajadores que lo requieran;</p>
    <p>4) Verificar que las indicaciones proporcionadas hayan quedado claras;</p>
    <p>5) Evitar interrumpir a los trabajadores cuando realicen sus respuestas, y</p>
    <p>6) Evitar conducir, persuadir o dirigir respuestas, y</p>
    <p><span class="text-info">c)    Después de la aplicación del cuestionario se deberá:</span></p>
    <p>1) Recoger cada cuestionario y verificar que hayan sido respondidos completamente, sin tachaduras o enmendaduras,
        y</p>
    <p>2) Comprobar que la cantidad de cuestionarios entregados corresponda con la cantidad de cuestionarios
        respondidos, y que a su vez corresponda con el tamaño mínimo de la muestra.</p>

